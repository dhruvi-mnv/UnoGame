package project.unogame;

/**
 *
 * @author dhruv
 */
public class Card {
    String color;
    String value;

    Card(String color, String value) {
        this.color = color;
        this.value = value;
    }

    @Override
    public String toString() {
        return color + " " + value;
    }
}
